package com.gzfgeh.viewpagecycle;

/**
 * Description: 广告栏对象
 * Created by guzhenfu on 16/2/21.
 */
public class BannerInfo extends Error{
	private String img;
	private String link;
	private int ot;

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public int getOt() {
		return ot;
	}

	public void setOt(int ot) {
		this.ot = ot;
	}
}
